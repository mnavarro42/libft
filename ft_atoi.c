#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	long	num;
	int		boolean;

	i = 0;
	boolean = 0;
	num = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' || str[i] == '\r'
			|| str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			boolean = 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + str[i] - 48;
		i++;
	}
	if (boolean == 1)
		num = num * -1;
	return (num);
}
