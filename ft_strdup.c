#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char			*s2;
	unsigned int	i;

	i = 0;
	while (s1[i])
		i++;
	s2 = malloc(i + 1);
	i = 0;
	if (s2 == NULL)
		return (0);
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
